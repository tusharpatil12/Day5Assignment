package com.bridgelabz.Assi;

public class ComputQuotientReminder {
	
public static void checkComputeQuotientReminder(int DIVIDER) {
		
		float quotient = DIVIDER / DIVIDER;
		float reminder = DIVIDER / DIVIDER;
		
		System.out.println("\nThe quotient is : " +quotient);
		System.out.println("\nThe reminder is : " +reminder);
	
	}

	public static void main(String[] args) {
		checkComputeQuotientReminder(100);
	}

}
